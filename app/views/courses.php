                        <h2 class="blockHeading">Malla Reddy Engineering College, Secunderabad<!-- --> Admission Process</h3>
                        <p>The Malla Reddy Engineering College, Secunderabad admissions are done at undergraduate and postgraduate level in B.Tech, M.Tech and MBA degree courses. Candidates who seek admissions to the MREC Secunderabad courses must meet the eligibility criteria for the courses. For MREC admissions at undergraduate level, the TS EAMCET - Telangana State Engineering Agriculture and Medical Common Entrance Test is accepted by the institute.&nbsp;</p>
                        <p>For  MREC Secunderabad  PG courses, candidates must qualify in  GATE  and TS PGECET for M.Tech programmes and a valid score card of TS ICET for MBA programmes. MREC Secunderabad admissions will be granted after the counselling round of respective entrance exams of ug and pg courses.</p>
